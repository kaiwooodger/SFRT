def baseline_g4_by_energy(reference: Dict, energies: List[int]) -> Dict[int, float]:
    out: Dict[int, float] = {}
    energy_block = reference["asymmetric_beamline"]["energies"]
    for energy in energies:
        key = str(energy)
        if key in energy_block:
            out[energy] = float(energy_block[key]["baseline_gradients_t_per_m"][3])
    return out


def z_column_for_mode(z_mode: str) -> str:
    if z_mode == "global_max":
        return "z_hat_global_cm"
    if z_mode == "integrated_xy":
        return "z_hat_integrated_cm"
    return "z_hat_on_axis_cm"


def sigma_columns_for_mode(sigma_mode: str) -> Tuple[str, str]:
    if sigma_mode == "integrated_xy":
        return "sigma_x_integrated_cm", "sigma_y_integrated_cm"
    if sigma_mode == "global_max":
        return "sigma_x_global_cm", "sigma_y_global_cm"
    return "sigma_x_on_axis_cm", "sigma_y_on_axis_cm"


def add_normalized_columns(
    df: pd.DataFrame,
    entrance_mode: str,
    z_mode: str,
    sigma_mode: str,
) -> pd.DataFrame:
    df = df.copy()
    entrance_col = "entrance_on_axis_pct" if entrance_mode == "on_axis" else "entrance_plane_max_pct"
    exit_col = "exit_on_axis_pct" if entrance_mode == "on_axis" else "exit_plane_max_pct"
    z_col = z_column_for_mode(z_mode)
    sigma_x_col, sigma_y_col = sigma_columns_for_mode(sigma_mode)

    df["entrance_selected_pct"] = df[entrance_col]
    df["exit_selected_pct"] = df[exit_col]
    df["z_hat_selected_plot_cm"] = df[z_col]
    df["sigma_x_selected_plot_cm"] = df[sigma_x_col]
    df["sigma_y_selected_plot_cm"] = df[sigma_y_col]

    def _safe_ratio(numerator: pd.Series, denominator: pd.Series) -> pd.Series:
        denom = denominator.replace(0.0, np.nan)
        return numerator / denom

    df["z_hat_selected_over_benchmark"] = _safe_ratio(df["z_hat_selected_plot_cm"], df["benchmark_z_hat_cm"])
    df["sigma_x_selected_over_benchmark"] = _safe_ratio(
        df["sigma_x_selected_plot_cm"], df["benchmark_sigma_x_cm"]
    )
    df["sigma_y_selected_over_benchmark"] = _safe_ratio(
        df["sigma_y_selected_plot_cm"], df["benchmark_sigma_y_cm"]
    )
    df["entrance_selected_over_benchmark"] = _safe_ratio(
        df["entrance_selected_pct"], df["benchmark_entrance_pct"]
    )

    df["z_hat_selected_dev_pct"] = 100.0 * (df["z_hat_selected_over_benchmark"] - 1.0)
    df["sigma_x_selected_dev_pct"] = 100.0 * (df["sigma_x_selected_over_benchmark"] - 1.0)
    df["sigma_y_selected_dev_pct"] = 100.0 * (df["sigma_y_selected_over_benchmark"] - 1.0)
    df["entrance_selected_dev_pct"] = 100.0 * (df["entrance_selected_over_benchmark"] - 1.0)
    return df


def compute_extra_dose_metrics(grid: np.ndarray) -> Dict[str, float]:
    nx, ny, nz = grid.shape
    cx = nx // 2
    cy = ny // 2
    on_axis = grid[cx, cy, :]
    peak_on_axis = float(np.max(on_axis))
    global_peak = float(np.max(grid))

    entrance_on_axis_pct = float("nan")
    exit_on_axis_pct = float("nan")
    if peak_on_axis > 0.0:
        entrance_on_axis_pct = 100.0 * float(on_axis[0]) / peak_on_axis
        exit_on_axis_pct = 100.0 * float(on_axis[-1]) / peak_on_axis

    entrance_plane_max_pct = float("nan")
    exit_plane_max_pct = float("nan")
    if global_peak > 0.0:
        entrance_plane_max_pct = 100.0 * float(np.max(grid[:, :, 0])) / global_peak
        exit_plane_max_pct = 100.0 * float(np.max(grid[:, :, nz - 1])) / global_peak

    return {
        "entrance_on_axis_pct": entrance_on_axis_pct,
        "exit_on_axis_pct": exit_on_axis_pct,
        "entrance_plane_max_pct": entrance_plane_max_pct,
        "exit_plane_max_pct": exit_plane_max_pct,
    }


def load_case_rows(args: argparse.Namespace, reference: Dict) -> Tuple[pd.DataFrame, List[str]]:
    if not args.manifest.exists():
        raise FileNotFoundError(f"Manifest not found: {args.manifest}")

    manifest = json.loads(args.manifest.read_text(encoding="utf-8"))
    requested = set(args.energies)
    rows: List[Dict[str, object]] = []
    skipped: List[str] = []

    for case in manifest.get("cases", []):
        energy = int(case.get("energy_mev", -1))
        if energy not in requested:
            continue

        csv_path = Path(case["dose_csv"])
        if not csv_path.exists():
            skipped.append(f"{case['case_id']}: missing CSV")
            continue

        try:
            grid, header = load_topas_grid(
                csv_path,
                retries=args.io_retries,
                retry_delay_sec=args.io_retry_delay_sec,
            )
            metrics = extract_metrics_from_grid(
                grid,
                dx_cm=float(header["dx_cm"]),
                dy_cm=float(header["dy_cm"]),
                dz_cm=float(header["dz_cm"]),
            )
            extra = compute_extra_dose_metrics(grid)
        except Exception as exc:  # pragma: no cover - read/runtime guard
            skipped.append(f"{case['case_id']}: {type(exc).__name__} ({exc})")
            continue

        gradients = case.get("gradients_t_per_m", [])
        g4 = float(gradients[3]) if len(gradients) >= 4 else float("nan")

        row: Dict[str, object] = {
            "case_id": case["case_id"],
            "energy_mev": energy,
            "g4_t_per_m": g4,
            "csv_file": str(csv_path.resolve()),
            "run_status": case.get("run_status", ""),
            **metrics,
            **extra,
        }

        benchmark = benchmark_for_energy(reference, energy)
        if benchmark is not None:
            score = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode=args.z_mode,
                sigma_mode=args.sigma_mode,
            )
            row.update(score)
            row["benchmark_z_hat_cm"] = float(benchmark["z_hat_cm"])
            row["benchmark_sigma_x_cm"] = float(benchmark["sigma_x_cm"])
            row["benchmark_sigma_y_cm"] = float(benchmark["sigma_y_cm"])
            row["benchmark_entrance_pct"] = float(benchmark["entrance_dose_pct"])

        rows.append(row)

    if not rows:
        raise RuntimeError("No readable case CSVs found for requested energies.")

    df = pd.DataFrame(rows)
    df = df.sort_values(["energy_mev", "g4_t_per_m", "case_id"], na_position="last")
    return df, skipped


def load_rows_from_case_metrics(args: argparse.Namespace, reference: Dict) -> Tuple[pd.DataFrame, List[str]]:
    if not args.case_metrics.exists():
        raise FileNotFoundError(f"case_metrics.csv not found: {args.case_metrics}")

    df = pd.read_csv(args.case_metrics)
    if "energy_mev" not in df.columns or "g4_t_per_m" not in df.columns:
        raise ValueError(f"Missing required columns in {args.case_metrics}")

    requested = set(args.energies)
    df = df[df["energy_mev"].astype(int).isin(requested)].copy()
    if df.empty:
        raise RuntimeError("No rows in case_metrics.csv for requested energies.")

    for col in [
        "case_id",
        "csv_file",
        "z_hat_cm",
        "z_hat_integrated_cm",
        "z_hat_on_axis_cm",
        "z_hat_global_cm",
        "sigma_x_cm",
        "sigma_y_cm",
        "sigma_x_integrated_cm",
        "sigma_y_integrated_cm",
        "sigma_x_on_axis_cm",
        "sigma_y_on_axis_cm",
        "sigma_x_global_cm",
        "sigma_y_global_cm",
        "entrance_on_axis_pct",
        "entrance_plane_max_pct",
    ]:
        if col not in df.columns:
            df[col] = np.nan
    if "z_hat_on_axis_cm" in df.columns and "z_hat_cm" in df.columns:
        df["z_hat_on_axis_cm"] = df["z_hat_on_axis_cm"].fillna(df["z_hat_cm"])
    if "z_hat_global_cm" in df.columns and "z_hat_cm" in df.columns:
        df["z_hat_global_cm"] = df["z_hat_global_cm"].fillna(df["z_hat_cm"])
    if "sigma_x_on_axis_cm" in df.columns and "sigma_x_cm" in df.columns:
        df["sigma_x_on_axis_cm"] = df["sigma_x_on_axis_cm"].fillna(df["sigma_x_cm"])
    if "sigma_y_on_axis_cm" in df.columns and "sigma_y_cm" in df.columns:
        df["sigma_y_on_axis_cm"] = df["sigma_y_on_axis_cm"].fillna(df["sigma_y_cm"])
    if "sigma_x_global_cm" in df.columns and "sigma_x_cm" in df.columns:
        df["sigma_x_global_cm"] = df["sigma_x_global_cm"].fillna(df["sigma_x_cm"])
    if "sigma_y_global_cm" in df.columns and "sigma_y_cm" in df.columns:
        df["sigma_y_global_cm"] = df["sigma_y_global_cm"].fillna(df["sigma_y_cm"])

    if "exit_on_axis_pct" not in df.columns:
        df["exit_on_axis_pct"] = np.nan
    if "exit_plane_max_pct" not in df.columns:
        df["exit_plane_max_pct"] = np.nan

    skipped: List[str] = []

    # Back-fill exit and (if needed) entrance normalization metrics from raw CSV files.
    for idx, row in df.iterrows():
        csv_path_raw = row.get("csv_file", "")
        if not isinstance(csv_path_raw, str) or not csv_path_raw:
            skipped.append(f"{row.get('case_id', idx)}: missing csv_file column value")
            continue
        csv_path = Path(csv_path_raw)
        if not csv_path.exists():
            skipped.append(f"{row.get('case_id', idx)}: missing CSV {csv_path}")
            continue

        need_extras = (
            pd.isna(row.get("exit_on_axis_pct"))
            or pd.isna(row.get("exit_plane_max_pct"))
            or pd.isna(row.get("entrance_on_axis_pct"))
            or pd.isna(row.get("entrance_plane_max_pct"))
            or pd.isna(row.get("z_hat_integrated_cm"))
            or pd.isna(row.get("sigma_x_integrated_cm"))
            or pd.isna(row.get("sigma_y_integrated_cm"))
        )
        if not need_extras:
            continue
        try:
            grid, header = load_topas_grid(
                csv_path,
                retries=args.io_retries,
                retry_delay_sec=args.io_retry_delay_sec,
            )
            extra = compute_extra_dose_metrics(grid)
            metrics = extract_metrics_from_grid(
                grid,
                dx_cm=float(header["dx_cm"]),
                dy_cm=float(header["dy_cm"]),
                dz_cm=float(header["dz_cm"]),
            )
            df.at[idx, "entrance_on_axis_pct"] = extra["entrance_on_axis_pct"]
            df.at[idx, "exit_on_axis_pct"] = extra["exit_on_axis_pct"]
            df.at[idx, "entrance_plane_max_pct"] = extra["entrance_plane_max_pct"]
            df.at[idx, "exit_plane_max_pct"] = extra["exit_plane_max_pct"]
            if pd.isna(row.get("z_hat_integrated_cm")):
                df.at[idx, "z_hat_integrated_cm"] = float(metrics["z_hat_integrated_cm"])
            if pd.isna(row.get("sigma_x_integrated_cm")):
                df.at[idx, "sigma_x_integrated_cm"] = float(metrics["sigma_x_integrated_cm"])
            if pd.isna(row.get("sigma_y_integrated_cm")):
                df.at[idx, "sigma_y_integrated_cm"] = float(metrics["sigma_y_integrated_cm"])
        except Exception as exc:  # pragma: no cover - runtime guard
            skipped.append(f"{row.get('case_id', idx)}: {type(exc).__name__} ({exc})")

    # Ensure Whitmore and score columns exist and are up-to-date.
    for col in [
        "benchmark_z_hat_cm",
        "benchmark_sigma_x_cm",
        "benchmark_sigma_y_cm",
        "benchmark_entrance_pct",
        "delta_z_hat_cm",
        "delta_sigma_x_cm",
        "delta_sigma_y_cm",
        "delta_entrance_pct",
        "weighted_error",
        "within_tolerance",
    ]:
        if col not in df.columns:
            df[col] = np.nan

    for idx, row in df.iterrows():
        energy = int(row["energy_mev"])
        benchmark = benchmark_for_energy(reference, energy)
        if benchmark is None:
            continue
        df.at[idx, "benchmark_z_hat_cm"] = float(benchmark["z_hat_cm"])
        df.at[idx, "benchmark_sigma_x_cm"] = float(benchmark["sigma_x_cm"])
        df.at[idx, "benchmark_sigma_y_cm"] = float(benchmark["sigma_y_cm"])
        df.at[idx, "benchmark_entrance_pct"] = float(benchmark["entrance_dose_pct"])

        try:
            metrics = {
                "z_hat_integrated_cm": float(row["z_hat_integrated_cm"]),
                "z_hat_on_axis_cm": float(row["z_hat_on_axis_cm"]),
                "z_hat_global_cm": float(row["z_hat_global_cm"]),
                "sigma_x_integrated_cm": float(row["sigma_x_integrated_cm"]),
                "sigma_y_integrated_cm": float(row["sigma_y_integrated_cm"]),
                "sigma_x_on_axis_cm": float(row["sigma_x_on_axis_cm"]),
                "sigma_y_on_axis_cm": float(row["sigma_y_on_axis_cm"]),
                "sigma_x_global_cm": float(row["sigma_x_global_cm"]),
                "sigma_y_global_cm": float(row["sigma_y_global_cm"]),
                "entrance_on_axis_pct": float(row["entrance_on_axis_pct"]),
                "entrance_plane_max_pct": float(row["entrance_plane_max_pct"]),
            }
            score = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode=args.z_mode,
                sigma_mode=args.sigma_mode,
            )
            for key, value in score.items():
                df.at[idx, key] = value
        except Exception as exc:  # pragma: no cover - row-level guard
            skipped.append(f"{row.get('case_id', idx)}: scoring error ({exc})")

    df = df.sort_values(["energy_mev", "g4_t_per_m", "case_id"], na_position="last")
    return df, skipped


def pick_best_cases(df: pd.DataFrame) -> pd.DataFrame:
    best_rows = []
    for energy, group in df.groupby("energy_mev"):
        valid = group.dropna(subset=["weighted_error"]) if "weighted_error" in group.columns else group
        if valid.empty:
            best_rows.append(group.iloc[0])
            continue
        best_rows.append(valid.loc[valid["weighted_error"].idxmin()])
    return pd.DataFrame(best_rows).sort_values("energy_mev")


def _format_case_label(row: pd.Series) -> str:
    base = f"{row['case_id']} (g4={row['g4_t_per_m']:.3f} T/m)"
    if "weighted_error" in row and pd.notna(row["weighted_error"]):
        base += f", error={row['weighted_error']:.2f}"
    return base


def plot_energy_panel(
    df_energy: pd.DataFrame,
    energy: int,
    benchmark: Dict[str, float],
    baseline_g4: float,
    entrance_mode: str,
    z_mode: str,
    sigma_mode: str,
    outdir: Path,
    dpi: int,
) -> Path:
    fig, axes = plt.subplots(2, 2, figsize=(12.5, 8.5), constrained_layout=True)
    fig.suptitle(f"Focused beamline Whitmore Comparison - {energy} MeV", fontsize=14, fontweight="bold")

    x = df_energy["g4_t_per_m"].astype(float).to_numpy()

    entrance_col = "entrance_on_axis_pct" if entrance_mode == "on_axis" else "entrance_plane_max_pct"
    exit_col = "exit_on_axis_pct" if entrance_mode == "on_axis" else "exit_plane_max_pct"
    z_col = z_column_for_mode(z_mode)
    sigma_x_col, sigma_y_col = sigma_columns_for_mode(sigma_mode)

    # Top-left: z_max depth trend.
    ax = axes[0, 0]
    ax.plot(x, df_energy[z_col], "o-", color="#1f77b4", label="TOPAS z_max")
    ax.axhline(float(benchmark["z_hat_cm"]), color="#d62728", linestyle="--", label="Whitmore z_max")
    ax.axvline(float(baseline_g4), color="#888888", linestyle=":", label="Whitmore Q4")
    ax.set_xlabel("Q4 gradient (T/m)")
    ax.set_ylabel("z_max (cm)")
    ax.set_title("Depth of Maximum Dose")
    ax.grid(alpha=0.25)
    ax.legend(fontsize=8)

    # Top-right: entrance and exit normalized dose trend.
    ax = axes[0, 1]
    ax.plot(x, df_energy[entrance_col], "o-", color="#2ca02c", label="Entrance / max (%)")
    ax.plot(x, df_energy[exit_col], "s-", color="#9467bd", label="Exit / max (%)")
    ax.axhline(
        float(benchmark["entrance_dose_pct"]),
        color="#d62728",
        linestyle="--",
        label="Whitmore entrance (%)",
    )
    ax.axvline(float(baseline_g4), color="#888888", linestyle=":")
    ax.set_xlabel("Q4 gradient (T/m)")
    ax.set_ylabel("Dose normalized to max (%)")
    ax.set_title("Entrance and Exit Dose Normalization")
    ax.grid(alpha=0.25)
    ax.text(
        0.02,
        0.03,
        "Paper exit-dose Whitmore not reported; trend shown for similarity check.",
        transform=ax.transAxes,
        fontsize=8,
    )
    ax.legend(fontsize=8, loc="best")

    # Bottom-left: sigma_x and sigma_y at z_max.
    ax = axes[1, 0]
    ax.plot(x, df_energy[sigma_x_col], "o-", color="#ff7f0e", label="TOPAS sigma_x")
    ax.plot(x, df_energy[sigma_y_col], "s-", color="#17becf", label="TOPAS sigma_y")
    ax.axhline(float(benchmark["sigma_x_cm"]), color="#ff7f0e", linestyle="--", label="Whitmore sigma_x")
    ax.axhline(float(benchmark["sigma_y_cm"]), color="#17becf", linestyle="--", label="Whitmore sigma_y")
    ax.axvline(float(baseline_g4), color="#888888", linestyle=":")
    ax.set_xlabel("Q4 gradient (T/m)")
    ax.set_ylabel("Sigma at z_max (cm)")
    ax.set_title("Transverse Spot Size at z_max")
    ax.grid(alpha=0.25)
    ax.legend(fontsize=8)

    # Bottom-right: percent deviation vs Whitmore.
    ax = axes[1, 1]
    z_dev = 100.0 * (df_energy[z_col] - float(benchmark["z_hat_cm"])) / float(benchmark["z_hat_cm"])
    sx_dev = 100.0 * (df_energy[sigma_x_col] - float(benchmark["sigma_x_cm"])) / float(benchmark["sigma_x_cm"])
    sy_dev = 100.0 * (df_energy[sigma_y_col] - float(benchmark["sigma_y_cm"])) / float(benchmark["sigma_y_cm"])
    en_dev = 100.0 * (
        df_energy[entrance_col] - float(benchmark["entrance_dose_pct"])
    ) / float(benchmark["entrance_dose_pct"])
    ax.plot(x, z_dev, "o-", label="z_max dev (%)")
    ax.plot(x, sx_dev, "o-", label="sigma_x dev (%)")
    ax.plot(x, sy_dev, "o-", label="sigma_y dev (%)")
    ax.plot(x, en_dev, "o-", label="entrance dev (%)")
    ax.axhline(0.0, color="black", linestyle="--", linewidth=1.0)
    ax.axvline(float(baseline_g4), color="#888888", linestyle=":")
    ax.set_xlabel("Q4 gradient (T/m)")
    ax.set_ylabel("Deviation from Whitmore (%)")
    ax.set_title("Relative Deviation Trend")
    ax.grid(alpha=0.25)
    ax.legend(fontsize=8, loc="best")

    out_file = outdir / f"energy_{energy}_benchmark_comparison.png"
    fig.savefig(out_file, dpi=dpi)
    plt.close(fig)
    return out_file


def plot_overlay_trends(
    df: pd.DataFrame,
    reference: Dict,
    energies: List[int],
    entrance_mode: str,
    z_mode: str,
    sigma_mode: str,
    outdir: Path,
    dpi: int,
) -> Path:
    fig, axes = plt.subplots(2, 2, figsize=(12.5, 8.5), constrained_layout=True)
    fig.suptitle("Normalized Trend Overlay Across Energies", fontsize=14, fontweight="bold")
    baseline_map = baseline_g4_by_energy(reference, energies)

    entrance_col = "entrance_on_axis_pct" if entrance_mode == "on_axis" else "entrance_plane_max_pct"
    z_col = z_column_for_mode(z_mode)
    sigma_x_col, sigma_y_col = sigma_columns_for_mode(sigma_mode)

    for energy in energies:
        block = df[df["energy_mev"] == energy].sort_values("g4_t_per_m")
        if block.empty:
            continue
        benchmark = benchmark_for_energy(reference, energy)
        if benchmark is None:
            continue
        delta_q4 = block["g4_t_per_m"] - float(baseline_map[energy])

        axes[0, 0].plot(
            delta_q4,
            block[z_col] / float(benchmark["z_hat_cm"]),
            "o-",
            label=f"{energy} MeV",
        )
        axes[0, 1].plot(
            delta_q4,
            block[entrance_col] / float(benchmark["entrance_dose_pct"]),
            "o-",
            label=f"{energy} MeV",
        )
        axes[1, 0].plot(
            delta_q4,
            block[sigma_x_col] / float(benchmark["sigma_x_cm"]),
            "o-",
            label=f"{energy} MeV",
        )
        axes[1, 1].plot(
            delta_q4,
            block[sigma_y_col] / float(benchmark["sigma_y_cm"]),
            "o-",
            label=f"{energy} MeV",
        )

    titles = [
        "z_max / Whitmore",
        "entrance / Whitmore",
        "sigma_x / Whitmore",
        "sigma_y / Whitmore",
    ]
    for ax, title in zip(axes.ravel(), titles):
        ax.axhline(1.0, color="black", linestyle="--", linewidth=1.0)
        ax.set_xlabel("Q4 - Whitmore Q4 (T/m)")
        ax.set_ylabel("Normalized value")
        ax.set_title(title)
        ax.grid(alpha=0.25)
        ax.legend(fontsize=8)

    out_file = outdir / "trend_overlay_normalized.png"
    fig.savefig(out_file, dpi=dpi)
    plt.close(fig)
    return out_file


def plot_best_depth_profiles(
    best_df: pd.DataFrame,
    reference: Dict,
    args: argparse.Namespace,
    outdir: Path,
    dpi: int,
) -> Path:
    energies = best_df["energy_mev"].astype(int).tolist()
    n = len(energies)
    fig, axes = plt.subplots(1, n, figsize=(4.8 * n, 4.2), constrained_layout=True)
    if n == 1:
        axes = [axes]

    fig.suptitle("Best-Case On-Axis Depth-Dose Profiles", fontsize=14, fontweight="bold")
    for ax, (_, row) in zip(axes, best_df.iterrows()):
        energy = int(row["energy_mev"])
        csv_path = Path(row["csv_file"])
        grid, header = load_topas_grid(
            csv_path,
            retries=args.io_retries,
            retry_delay_sec=args.io_retry_delay_sec,
        )
        nx, ny, nz = grid.shape
        cx, cy = nx // 2, ny // 2
        on_axis = grid[cx, cy, :]
        peak = float(np.max(on_axis))
        if peak <= 0:
            depth = (np.arange(nz) + 0.5) * float(header["dz_cm"])
            norm = np.zeros_like(depth)
        else:
            depth = (np.arange(nz) + 0.5) * float(header["dz_cm"])
            norm = on_axis / peak

        benchmark = benchmark_for_energy(reference, energy)
        benchmark_z = float(benchmark["z_hat_cm"]) if benchmark is not None else float("nan")
        ax.plot(depth, norm, color="#1f77b4", label=_format_case_label(row))
        if math.isfinite(benchmark_z):
            ax.axvline(benchmark_z, color="#d62728", linestyle="--", label="Whitmore z_max")
        ax.set_title(f"{energy} MeV")
        ax.set_xlabel("Depth z (cm)")
        ax.set_ylabel("On-axis dose / max")
        ax.grid(alpha=0.25)
        ax.set_ylim(0.0, 1.05)
        ax.legend(fontsize=8, loc="best")

    out_file = outdir / "best_case_depth_profiles.png"
    fig.savefig(out_file, dpi=dpi)
    plt.close(fig)
    return out_file


def main() -> int:
    args = parse_args()
    reference = load_reference(args.reference)
    outdir = args.outdir
    outdir.mkdir(parents=True, exist_ok=True)

    if args.case_metrics.exists():
        df, skipped = load_rows_from_case_metrics(args, reference)
    else:
        df, skipped = load_case_rows(args, reference)
    df = add_normalized_columns(
        df=df,
        entrance_mode=args.entrance_mode,
        z_mode=args.z_mode,
        sigma_mode=args.sigma_mode,
    )
    plot_data_file = outdir / "plot_metrics_all_cases.csv"
    df.to_csv(plot_data_file, index=False)

    baseline_map = baseline_g4_by_energy(reference, args.energies)
    figure_paths: List[Path] = []

    for energy in args.energies:
        block = df[df["energy_mev"] == energy].sort_values("g4_t_per_m")
        if block.empty:
            continue
        benchmark = benchmark_for_energy(reference, energy)
        if benchmark is None:
            continue
        figure_paths.append(
            plot_energy_panel(
                df_energy=block,
                energy=energy,
                benchmark=benchmark,
                baseline_g4=float(baseline_map[energy]),
                entrance_mode=args.entrance_mode,
                z_mode=args.z_mode,
                sigma_mode=args.sigma_mode,
                outdir=outdir,
                dpi=args.dpi,
            )
        )

    if not figure_paths:
        print("No per-energy figures were generated (no usable Whitmore-comparable data).")
        return 1

    overlay_path = plot_overlay_trends(
        df,
        reference,
        args.energies,
        args.entrance_mode,
        args.z_mode,
        args.sigma_mode,
        outdir,
        args.dpi,
    )
    best_df = pick_best_cases(df)
    best_file = outdir / "best_cases_for_plots.csv"
    best_df.to_csv(best_file, index=False)
    best_profiles = plot_best_depth_profiles(best_df, reference, args, outdir, args.dpi)

    print(f"Plot metrics table: {plot_data_file}")
    print(f"Best cases table: {best_file}")
    for path in figure_paths:
        print(f"Figure: {path}")
    print(f"Figure: {overlay_path}")
    print(f"Figure: {best_profiles}")

    if skipped:
        print(f"Skipped cases: {len(skipped)}")
        for note in skipped[:15]:
            print(f"  {note}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
