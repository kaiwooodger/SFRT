def manifest_mode(args: argparse.Namespace, reference: Dict) -> int:
    if not args.manifest.exists():
        raise FileNotFoundError(f"Manifest not found: {args.manifest}")
    manifest = json.loads(args.manifest.read_text(encoding="utf-8"))
    cases = manifest.get("cases", [])
    if not cases:
        raise ValueError(f"No cases found in manifest: {args.manifest}")

    rows: List[Dict[str, object]] = []
    missing: List[str] = []
    unreadable: List[Tuple[str, str]] = []
    for case in cases:
        csv_path = Path(case["dose_csv"])
        if not csv_path.exists():
            missing.append(case["case_id"])
            continue
        gradients = case.get("gradients_t_per_m", [])
        if not gradients:
            gradients = case.get("paper_gradients_t_per_m", [])
        if not gradients:
            gradients = case.get("gradient_x_t_per_m", [])
        g4_tpm = float(gradients[3]) if len(gradients) >= 4 else float("nan")
        try:
            rows.append(
                analyze_case(
                    csv_path=csv_path,
                    reference=reference,
                    energy_mev=int(case["energy_mev"]),
                    g4_tpm=g4_tpm,
                    args=args,
                    case_id=case["case_id"],
                )
            )
        except Exception as exc:
            unreadable.append((case["case_id"], f"{type(exc).__name__}: {exc}"))
            continue

    outdir = args.outdir
    outdir.mkdir(parents=True, exist_ok=True)
    if not rows:
        print("No existing CSV files were found for analysis.")
        print(f"Missing outputs for {len(missing)} case(s).")
        if unreadable:
            print(f"Unreadable CSV files for {len(unreadable)} case(s).")
        return 1

    df = pd.DataFrame(rows)
    df = df.sort_values(["energy_mev", "g4_t_per_m", "case_id"], na_position="last")
    case_metrics_file = outdir / "case_metrics.csv"
    df.to_csv(case_metrics_file, index=False)

    best_rows = []
    for energy, group in df.groupby("energy_mev"):
        valid = group.dropna(subset=["weighted_error"])
        if valid.empty:
            continue
        best_rows.append(valid.loc[valid["weighted_error"].idxmin()])
    best_df = pd.DataFrame(best_rows) if best_rows else pd.DataFrame()

    best_file = outdir / "best_per_energy.csv"
    best_df.to_csv(best_file, index=False)

    report_file = outdir / "validation_report.md"
    write_validation_report(report_file, best_df, args, total_cases=len(df))

    print(f"Case metrics: {case_metrics_file}")
    print(f"Best per energy: {best_file}")
    print(f"Validation report: {report_file}")
    if missing:
        print(f"Skipped (missing CSV): {len(missing)} case(s)")
    if unreadable:
        print(f"Skipped (unreadable CSV): {len(unreadable)} case(s)")
        for case_id, reason in unreadable[:10]:
            print(f"  {case_id}: {reason}")

    if not best_df.empty:
        for _, row in best_df.sort_values("energy_mev").iterrows():
            state = "PASS" if bool(row.get("within_tolerance", False)) else "FAIL"
            print(
                f"E={int(row['energy_mev'])} MeV | best={row['case_id']} | "
                f"g4={row['g4_t_per_m']:.4f} T/m | error={row['weighted_error']:.3f} | {state}"
            )
    return 0


def direct_csv_mode(args: argparse.Namespace, reference: Dict) -> int:
    if not args.csv:
        return 1
    energy = args.energy if args.energy > 0 else None
    g4 = None
    if not math.isnan(args.g4):
        g4 = float(args.g4)

    rows = []
    for csv_path in args.csv:
        if not csv_path.exists():
            print(f"Missing CSV: {csv_path}")
            continue
        try:
            rows.append(
                analyze_case(
                    csv_path=csv_path,
                    reference=reference,
                    energy_mev=energy,
                    g4_tpm=g4,
                    args=args,
                )
            )
        except Exception as exc:
            print(f"Unreadable CSV: {csv_path} ({type(exc).__name__}: {exc})")
            continue
    if not rows:
        return 1

    df = pd.DataFrame(rows)
    display_cols = [
        "case_id",
        "energy_mev",
        "g4_t_per_m",
        "sigma_fit_mode_used",
        "z_hat_integrated_cm",
        "z_hat_on_axis_cm",
        "z_hat_global_cm",
        "z_hat_selected_cm",
        "sigma_x_integrated_cm",
        "sigma_x_on_axis_cm",
        "sigma_x_global_cm",
        "sigma_x_independent_min_cm",
        "z_hat_sigma_x_min_cm",
        "sigma_x_selected_cm",
        "sigma_x_selected_depth_cm",
        "sigma_y_integrated_cm",
        "sigma_y_on_axis_cm",
        "sigma_y_global_cm",
        "sigma_y_independent_min_cm",
        "z_hat_sigma_y_min_cm",
        "sigma_y_selected_cm",
        "sigma_y_selected_depth_cm",
        "sigma_x_entrance_2d_cm",
        "sigma_y_entrance_2d_cm",
        "entrance_integrated_pct",
        "entrance_on_axis_pct",
        "entrance_plane_max_pct",
        "exit_integrated_pct",
        "exit_on_axis_pct",
        "exit_plane_max_pct",
    ]
    extra_cols = [c for c in ("weighted_error", "within_tolerance") if c in df.columns]
    print(df[display_cols + extra_cols].to_string(index=False))
    return 0


def main() -> int:
    args = parse_args()
    reference = load_reference(args.reference)
    if args.csv:
        return direct_csv_mode(args, reference)
    return manifest_mode(args, reference)


if __name__ == "__main__":
    raise SystemExit(main())
