def find_table3_row(reference: Dict, energy_mev: int, g4_tpm: float) -> Optional[Dict]:
    entry = reference["asymmetric_beamline"]["energies"].get(str(energy_mev))
    if not entry:
        return None
    for row in entry["supplementary_table3_scan"]:
        if abs(float(row["g4_t_per_m"]) - g4_tpm) < 1e-9:
            return row
    return None


def benchmark_for_energy(reference: Dict, energy_mev: int) -> Optional[Dict]:
    entry = reference["asymmetric_beamline"]["energies"].get(str(energy_mev))
    if not entry:
        return None
    return entry["benchmark_metrics_table2"]


def score_against_benchmark(
    metrics: Dict[str, float],
    benchmark: Dict[str, float],
    entrance_mode: str,
    tol_z_cm: float,
    tol_sx_cm: float,
    tol_sy_cm: float,
    tol_ent_pct: float,
    z_mode: str = "on_axis",
    sigma_mode: str = "on_axis",
) -> Dict[str, float]:
    if entrance_mode == "on_axis":
        entrance_value = metrics["entrance_on_axis_pct"]
    elif entrance_mode == "integrated_xy":
        entrance_value = metrics["entrance_integrated_pct"]
    else:
        entrance_value = metrics["entrance_plane_max_pct"]

    z_value = (
        float(metrics["z_hat_integrated_cm"])
        if z_mode == "integrated_xy"
        else (
            float(metrics["z_hat_global_cm"])
            if z_mode == "global_max"
            else float(metrics["z_hat_on_axis_cm"])
        )
    )
    sigma_x_value = (
        float(metrics["sigma_x_integrated_cm"])
        if sigma_mode == "integrated_xy"
        else (
            float(metrics["sigma_x_global_cm"])
            if sigma_mode == "global_max"
            else (
                float(metrics["sigma_x_independent_min_cm"])
                if sigma_mode == "independent_xy_min"
                else float(metrics["sigma_x_on_axis_cm"])
            )
        )
    )
    sigma_y_value = (
        float(metrics["sigma_y_integrated_cm"])
        if sigma_mode == "integrated_xy"
        else (
            float(metrics["sigma_y_global_cm"])
            if sigma_mode == "global_max"
            else (
                float(metrics["sigma_y_independent_min_cm"])
                if sigma_mode == "independent_xy_min"
                else float(metrics["sigma_y_on_axis_cm"])
            )
        )
    )
    if sigma_mode == "independent_xy_min":
        sigma_x_depth_value = float(metrics["z_hat_sigma_x_min_cm"])
        sigma_y_depth_value = float(metrics["z_hat_sigma_y_min_cm"])
    elif sigma_mode == "on_axis":
        sigma_x_depth_value = float(metrics["z_hat_on_axis_cm"])
        sigma_y_depth_value = float(metrics["z_hat_on_axis_cm"])
    elif sigma_mode == "global_max":
        sigma_x_depth_value = float(metrics["z_hat_global_cm"])
        sigma_y_depth_value = float(metrics["z_hat_global_cm"])
    else:
        sigma_x_depth_value = float(metrics["z_hat_integrated_cm"])
        sigma_y_depth_value = float(metrics["z_hat_integrated_cm"])
    dz = z_value - float(benchmark["z_hat_cm"])
    dsx = sigma_x_value - float(benchmark["sigma_x_cm"])
    dsy = sigma_y_value - float(benchmark["sigma_y_cm"])
    dent = entrance_value - float(benchmark["entrance_dose_pct"])

    terms = [
        (dz / tol_z_cm) ** 2,
        (dsx / tol_sx_cm) ** 2,
        (dsy / tol_sy_cm) ** 2,
        (dent / tol_ent_pct) ** 2,
    ]
    weighted_error = math.sqrt(sum(terms))

    within = (
        abs(dz) <= tol_z_cm
        and abs(dsx) <= tol_sx_cm
        and abs(dsy) <= tol_sy_cm
        and abs(dent) <= tol_ent_pct
    )
    return {
        "delta_z_hat_cm": dz,
        "delta_sigma_x_cm": dsx,
        "delta_sigma_y_cm": dsy,
        "delta_entrance_pct": dent,
        "entrance_mode_used": entrance_mode,
        "entrance_selected_pct": float(entrance_value),
        "weighted_error": weighted_error,
        "within_tolerance": bool(within),
        "z_mode_used": z_mode,
        "z_hat_selected_cm": z_value,
        "sigma_mode_used": sigma_mode,
        "sigma_x_selected_cm": sigma_x_value,
        "sigma_y_selected_cm": sigma_y_value,
        "sigma_x_selected_depth_cm": sigma_x_depth_value,
        "sigma_y_selected_depth_cm": sigma_y_depth_value,
    }


def analyze_case(
    csv_path: Path,
    reference: Dict,
    energy_mev: Optional[int],
    g4_tpm: Optional[float],
    args: argparse.Namespace,
    case_id: str = "",
) -> Dict[str, object]:
    grid, header = load_topas_grid(
        csv_path,
        retries=args.io_retries,
        retry_delay_sec=args.io_retry_delay_sec,
    )
    metrics = extract_metrics_from_grid(
        grid,
        dx_cm=float(header["dx_cm"]),
        dy_cm=float(header["dy_cm"]),
        dz_cm=float(header["dz_cm"]),
        sigma_fit_mode=args.sigma_fit_mode,
    )

    row: Dict[str, object] = {
        "case_id": case_id if case_id else csv_path.stem,
        "csv_file": str(csv_path.resolve()),
        "energy_mev": energy_mev,
        "g4_t_per_m": g4_tpm,
        **metrics,
    }

    if energy_mev is not None:
        benchmark = benchmark_for_energy(reference, energy_mev)
        if benchmark is not None:
            scored = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode=args.z_mode,
                sigma_mode=args.sigma_mode,
            )
            row.update(scored)
            row["benchmark_sigma_x_cm"] = float(benchmark["sigma_x_cm"])
            row["benchmark_sigma_y_cm"] = float(benchmark["sigma_y_cm"])
            row["benchmark_z_hat_cm"] = float(benchmark["z_hat_cm"])
            row["benchmark_entrance_pct"] = float(benchmark["entrance_dose_pct"])
            row["delta_z_hat_on_axis_cm"] = float(metrics["z_hat_on_axis_cm"]) - float(
                benchmark["z_hat_cm"]
            )
            row["delta_z_hat_integrated_cm"] = float(metrics["z_hat_integrated_cm"]) - float(
                benchmark["z_hat_cm"]
            )
            row["delta_z_hat_global_cm"] = float(metrics["z_hat_global_cm"]) - float(
                benchmark["z_hat_cm"]
            )
            row["delta_sigma_x_integrated_cm"] = float(metrics["sigma_x_integrated_cm"]) - float(
                benchmark["sigma_x_cm"]
            )
            row["delta_sigma_x_on_axis_cm"] = float(metrics["sigma_x_on_axis_cm"]) - float(
                benchmark["sigma_x_cm"]
            )
            row["delta_sigma_x_global_cm"] = float(metrics["sigma_x_global_cm"]) - float(
                benchmark["sigma_x_cm"]
            )
            row["delta_sigma_x_independent_cm"] = float(
                metrics["sigma_x_independent_min_cm"]
            ) - float(benchmark["sigma_x_cm"])
            row["delta_sigma_y_integrated_cm"] = float(metrics["sigma_y_integrated_cm"]) - float(
                benchmark["sigma_y_cm"]
            )
            row["delta_sigma_y_on_axis_cm"] = float(metrics["sigma_y_on_axis_cm"]) - float(
                benchmark["sigma_y_cm"]
            )
            row["delta_sigma_y_global_cm"] = float(metrics["sigma_y_global_cm"]) - float(
                benchmark["sigma_y_cm"]
            )
            row["delta_sigma_y_independent_cm"] = float(
                metrics["sigma_y_independent_min_cm"]
            ) - float(benchmark["sigma_y_cm"])

            scored_integrated = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode="integrated_xy",
                sigma_mode="integrated_xy",
            )
            row["weighted_error_integrated"] = float(scored_integrated["weighted_error"])
            row["within_tolerance_integrated"] = bool(scored_integrated["within_tolerance"])

            scored_on_axis = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode="on_axis",
                sigma_mode="on_axis",
            )
            scored_global = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode="global_max",
                sigma_mode="global_max",
            )
            row["weighted_error_on_axis"] = float(scored_on_axis["weighted_error"])
            row["within_tolerance_on_axis"] = bool(scored_on_axis["within_tolerance"])
            row["weighted_error_global"] = float(scored_global["weighted_error"])
            row["within_tolerance_global"] = bool(scored_global["within_tolerance"])
            scored_independent = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode=args.z_mode,
                sigma_mode="independent_xy_min",
            )
            row["weighted_error_sigma_independent"] = float(
                scored_independent["weighted_error"]
            )
            row["within_tolerance_sigma_independent"] = bool(
                scored_independent["within_tolerance"]
            )
            scored_z_global_sigma_on_axis = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode="global_max",
                sigma_mode="on_axis",
            )
            scored_z_on_axis_sigma_global = score_against_benchmark(
                metrics=metrics,
                benchmark=benchmark,
                entrance_mode=args.entrance_mode,
                tol_z_cm=args.tol_z_cm,
                tol_sx_cm=args.tol_sigma_x_cm,
                tol_sy_cm=args.tol_sigma_y_cm,
                tol_ent_pct=args.tol_entrance_pct,
                z_mode="on_axis",
                sigma_mode="global_max",
            )
            row["weighted_error_z_global_sigma_on_axis"] = float(
                scored_z_global_sigma_on_axis["weighted_error"]
            )
            row["weighted_error_z_on_axis_sigma_global"] = float(
                scored_z_on_axis_sigma_global["weighted_error"]
            )

        if g4_tpm is not None and not math.isnan(g4_tpm):
            ref_scan_row = find_table3_row(reference, energy_mev, g4_tpm)
            if ref_scan_row is not None:
                row["ref_table3_z_hat_cm"] = float(ref_scan_row["z_hat_cm"])
                row["delta_z_hat_vs_table3_cm"] = (
                    float(metrics["z_hat_cm"]) - float(ref_scan_row["z_hat_cm"])
                )
            else:
                row["ref_table3_z_hat_cm"] = float("nan")
                row["delta_z_hat_vs_table3_cm"] = float("nan")

    return row


