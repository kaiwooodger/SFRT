AXIS_HEADER_RE = re.compile(
    r"^#\s*([XYZ])\s+in\s+(\d+)\s+bins\s+of\s+([0-9eE+\-.]+)\s+cm\s*$",
    re.IGNORECASE,
)
REPORT_HEADER_RE = re.compile(
    r"^#\s*(.+?)\s*\(\s*([^)]+?)\s*\)\s*:\s*(.+?)\s*$",
    re.IGNORECASE,
)


